/** Parse a dash-separated code string like "200-350-360" into decoded entries */
export function decodeCodigos(raw: string | null): { codigo: string; descripcion: string }[] {
  if (!raw) return []
  return raw
    .split('-')
    .map(c => c.trim())
    .filter(c => c !== '')
    .map(codigo => ({
      codigo,
      descripcion: CATALOGO_USOS[codigo] || 'Código no identificado',
    }))
}
