CREATE SCHEMA IF NOT EXISTS avaluos;

COMMENT ON SCHEMA avaluos IS 'Esquema para tablas relacionadas con cálculo de avalúos catastrales (Ordenanza 0060-2023)';

-- -----------------------------------------------------------------------------
-- 2. CREAR TABLA valor_suelo_base
-- -----------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS avaluos.valor_suelo_base (
    -- Identificación
    id SERIAL PRIMARY KEY,
    codigo_sh INTEGER NOT NULL, -- Código Sector Homogéneo (antes codigo_manzana)
    descripcion TEXT, -- Nombre descriptivo del sector (ej: "Centro Histórico", "Ampliación")
    
    -- Valor económico
    valor_m2 NUMERIC(10, 2) NOT NULL, -- Valor base por metro cuadrado (USD/m²)
    bienio VARCHAR(10) NOT NULL DEFAULT '2024-2025', -- Período de vigencia
    
    -- Metadatos de origen
    tabla_origen VARCHAR(50) DEFAULT 'Tabla 3', -- Referencia a tabla del PDF
    fuente_ordenanza VARCHAR(100) DEFAULT 'Ordenanza 0060-2023', -- Ordenanza municipal
    
    -- Control de vigencia
    vigencia_desde DATE DEFAULT '2024-01-01',
    vigencia_hasta DATE DEFAULT '2025-12-31',
    
    -- Notas adicionales
    observacion TEXT,
    
    -- Auditoría
    creado_en TIMESTAMP DEFAULT NOW(),
    actualizado_en TIMESTAMP DEFAULT NOW(),
    
    -- Constraints
    CONSTRAINT uk_valor_suelo_base_codigo_bienio UNIQUE (codigo_sh, bienio),
    CONSTRAINT chk_valor_m2_positivo CHECK (valor_m2 > 0),
    CONSTRAINT chk_codigo_sh_rango CHECK (codigo_sh BETWEEN 1 AND 999)
);

-- Índices para búsqueda eficiente
CREATE INDEX IF NOT EXISTS idx_valor_suelo_base_codigo_sh 
    ON avaluos.valor_suelo_base(codigo_sh);

CREATE INDEX IF NOT EXISTS idx_valor_suelo_base_bienio 
    ON avaluos.valor_suelo_base(bienio);

CREATE INDEX IF NOT EXISTS idx_valor_suelo_base_vigencia 
    ON avaluos.valor_suelo_base(vigencia_desde, vigencia_hasta);

-- Comentarios de documentación
COMMENT ON TABLE avaluos.valor_suelo_base IS 'Valor base del suelo por sector homogéneo (Tabla 3 de Ordenanza 0060-2023). Extensión de costos_manzana con metadatos adicionales.';

COMMENT ON COLUMN avaluos.valor_suelo_base.codigo_sh IS 'Código del Sector Homogéneo (1-304+). Corresponde a codigo_manzana en tabla legacy.';
COMMENT ON COLUMN avaluos.valor_suelo_base.valor_m2 IS 'Valor base del suelo en USD por metro cuadrado. Rango típico: 73-336 USD/m².';
COMMENT ON COLUMN avaluos.valor_suelo_base.bienio IS 'Período de vigencia del valor (ej: 2024-2025, 2026-2027).';
COMMENT ON COLUMN avaluos.valor_suelo_base.descripcion IS 'Nombre descriptivo del sector (opcional, para facilitar identificación).';

